def dice_coef(y_true, y_pred, smooth=1.):
    # y_true_f = K.flatten(y_true)
    # y_pred_f = K.flatten(y_pred)
    y_true_f = y_true
    y_pred_f = y_pred
    intersection = np.sum(y_true_f * y_pred_f)
    return (2. * intersection + smooth) / (np.sum(y_true_f) + np.sum(y_pred_f) + smooth)
